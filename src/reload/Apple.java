package reload;

public class Apple {
     
	public String name;
     
    public String color;
    
    public String weiDao;
    
	public Apple(String name, String color, String weiDao) {
		super();
		this.name = name;
		this.color = color;
		this.weiDao = weiDao;
	}

	public String getName() {
		return name;
	}

	public String getColor() {
		return color;
	}

	public String getWeiDao() {
		return weiDao;
	}
    
    
}
