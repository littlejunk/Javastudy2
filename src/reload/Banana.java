package reload;

public class Banana {
	public String name;
    
    public String color;
    
    public String weiDao;
    
	public Banana(String name, String color, String weiDao) {
		super();
		this.name = name;
		this.color = color;
		this.weiDao = weiDao;
	}

	public String getName() {
		return name;
	}

	public String getColor() {
		return color;
	}

	public String getWeiDao() {
		return weiDao;
	}
}
